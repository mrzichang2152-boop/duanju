def get_system_prompt(instruction: str) -> str:
    """
    根据 instruction 中的关键词自动选择最匹配的行业级 Prompt
    """
    instruction_lower = instruction.lower()

    if instruction_lower == "extract_resources":
        return PROMPT_EXTRACT_RESOURCES
    
    if instruction_lower == "step2_modify":
        return PROMPT_MODIFY_RESOURCES

    if instruction_lower == "generate_storyboard":
        return PROMPT_STORYBOARD

    
    # 关键词匹配逻辑 (优先级从上到下)
    if "重生" in instruction_lower and "复仇" in instruction_lower:
        return PROMPT_REBIRTH_REVENGE
    
    if "古风" in instruction_lower and "甜宠" in instruction_lower:
        return PROMPT_ANCIENT_SWEET
    
    if "古风" in instruction_lower and "权谋" in instruction_lower:
        return PROMPT_ANCIENT_POWER
        
    if "赘婿" in instruction_lower or "战神" in instruction_lower or "龙王" in instruction_lower:
        return PROMPT_SON_IN_LAW
        
    if "霸总" in instruction_lower or "总裁" in instruction_lower:
        return PROMPT_CEO_SWEET
        
    if "悬疑" in instruction_lower or "反转" in instruction_lower or "探案" in instruction_lower:
        return PROMPT_SUSPENSE
        
    if "系统" in instruction_lower or "升级" in instruction_lower or "异能" in instruction_lower:
        return PROMPT_SYSTEM
        
    if "家庭" in instruction_lower or "伦理" in instruction_lower or "婆媳" in instruction_lower:
        return PROMPT_FAMILY
        
    if "励志" in instruction_lower or "创业" in instruction_lower or "逆袭" in instruction_lower:
        return PROMPT_REALITY
        
    if "搞笑" in instruction_lower or "沙雕" in instruction_lower:
        return PROMPT_COMEDY
        
    if "仙侠" in instruction_lower or "修仙" in instruction_lower:
        return PROMPT_XIANXIA
        
    if "校园" in instruction_lower or "青春" in instruction_lower or "暗恋" in instruction_lower:
        return PROMPT_CAMPUS
        
    if "真假" in instruction_lower or "豪门" in instruction_lower:
        return PROMPT_FAKE_HEIR
        
    if "末世" in instruction_lower or "丧尸" in instruction_lower:
        return PROMPT_DOOMSDAY
        
    # 如果只有 "重生" 但没有复仇，也倾向于重生复仇 (因为最常见)
    if "重生" in instruction_lower:
        return PROMPT_REBIRTH_REVENGE

    return PROMPT_DEFAULT
